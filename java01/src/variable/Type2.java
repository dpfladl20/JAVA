package variable;

public class Type2 {
	public static void main(String[] args) {
		// 변수를 활용해서 이름, 출생년도, 키
		/*
		 이름 : 000
		 출생년도 : 0000년
		 키 : 000.00cm
		 */
		String name = "이예림";
		int year = 1994;
		double height = 163.4;
		
		System.out.println("이름:"+name);
		System.out.println("출생년도:"+year);
		System.out.println("키:"+height+"cm");
		
		
	}
}
